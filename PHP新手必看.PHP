啰嗦了这么久，开始讲了：

1、你不一定要遵守 PSR1-4 的PHP编程规范，但你必须了解他们是什么东西，具体有哪些作用。

2、你必须清晰的认识到，自己今后是想往后端、还是前端发展(不管你的目标是不是全栈，初期都要有个先后)。

3、可能在这个阶段，身边会有很多人告诉你代码的优化还有性能问题，但我以一个过来人的身份告诉你：
      先学会如果提高代码的可读性，再思考如何提升代码的执行效率。

4、你不需要拥有代码洁癖的病症，但你需要规范自己的编程习惯，这可以让你更好的融入开发团队当中
      这是我个人平时的编程习惯：http://www.junphp.com/Blog/Index.php/News/Nei/Pid/26/Id/168.html

5、可能当你入门级的时候，已经在一家外包公司中工作，这个时候你要记住，不管公司如何对不住你，都不要做留后门删库跑路的事情，并且不管是任何公司，都不要抱着大牛会主动教你的心态(当然你是个女生除外)，每天主动留给自己一些学习的时间，并且要逐渐学会掌控项目进度与分配项目进度时间。